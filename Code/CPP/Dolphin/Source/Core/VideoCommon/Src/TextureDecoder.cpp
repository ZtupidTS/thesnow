u64 TexDecoder_GetHash64(const u8 *src, int len, u32 samples)
{
	const u32 m = 0x5bd1e995;
	const int r = 24;
	
	u32 h1 = len;
	u32 h2 = 0;

	u32 Step = (len / 4);
	const u32 * data = (const u32 *)src;
	const u32 * end = data + Step;
	const u8 * uEnd = (const u8 *)end;
	if(samples == 0) samples = Step;
	Step = Step / samples;

	if(Step < 2) Step = 2;	

	while(data < end)
	{
		u32 k1 = data[0];
		k1 *= m; 
		k1 ^= k1 >> r; 
		k1 *= m;
		h1 *= m; 
		h1 ^= k1;
		

		u32 k2 = data[1];
		k2 *= m; 
		k2 ^= k2 >> r; 
		k2 *= m;
		h2 *= m; 
		h2 ^= k2;
		data+=Step;
	}

	if(len & 7 > 3)
	{
		u32 k1 = *(end - 1);
		k1 *= m; 
		k1 ^= k1 >> r; 
		k1 *= m;
		h1 *= m; 
		h1 ^= k1;
		len -= 4;
	}

	switch(len & 3)
	{
	case 3: h2 ^= uEnd[2] << 16;
	case 2: h2 ^= uEnd[1] << 8;
	case 1: h2 ^= uEnd[0];
			h2 *= m;
	};

	h1 ^= h2 >> 18; h1 *= m;
	h2 ^= h1 >> 22; h2 *= m;
	h1 ^= h2 >> 17; h1 *= m;
	h2 ^= h1 >> 19; h2 *= m;

	u64 h = h1;

	h = (h << 32) | h2;

	return h;
}


