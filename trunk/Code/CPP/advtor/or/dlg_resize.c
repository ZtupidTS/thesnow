void resizeDialogControls(HWND hDlg,resize_info_t *resizeInfo)
{	GetWindowRect(hDlg,&rParent);
	delta.x=0;delta.y=0;ClientToScreen(hDlg,&delta);
	for(i=0;resizeInfo[i].ctrlId;i++)
	{	GetWindowRect(GetDlgItem(hDlg,resizeInfo[i].ctrlId),&rCtrl);

		if(resizeInfo[i].flags&RESIZE_MASK_POSX_NEED_REF)
		{	GetWindowRect(resizeInfo[i].refPosXControl?GetDlgItem(hDlg,resizeInfo[i].refPosXControl):hDlg,&rRef);
			if(resizeInfo[i].flags&RESIZE_FLAG_SAME_DISTANCE_CONTROL_POS_LEFT_POS_X)
				newSize.left=rRef.left-resizeInfo[i].reference.right;
			else if(resizeInfo[i].flags&RESIZE_FLAG_SAME_DISTANCE_CONTROL_POS_RIGHT_POS_X)
				newSize.left=rRef.right-resizeInfo[i].reference.right;
			else if(resizeInfo[i].flags&RESIZE_FLAG_SAME_DISTANCE_CONTROL_MIDDLE_POS_X)
				newSize.left=((rRef.right+rRef.left)>>1)-resizeInfo[i].reference.right;
			else if(resizeInfo[i].flags&RESIZE_FLAG_RELATIVE_TO_CONTROL_POS_LEFT_POS_X)
				newSize.left=(((rRef.left-rParent.left)*resizeInfo[i].reference.right)>>12)+rParent.left;
			else if(resizeInfo[i].flags&RESIZE_FLAG_RELATIVE_TO_CONTROL_POS_RIGHT_POS_X)
				newSize.left=(((rParent.right-rRef.right)*resizeInfo[i].reference.right)>>12)+rParent.left;
		}
		else if(resizeInfo[i].flags&RESIZE_FLAG_FIXED_POS_X)
		{	newSize.left=resizeInfo[i].reference.right;
		}
		else newSize.left=rCtrl.left;

		if(resizeInfo[i].flags&RESIZE_MASK_POSY_NEED_REF)
		{	GetWindowRect(resizeInfo[i].refPosYControl?GetDlgItem(hDlg,resizeInfo[i].refPosYControl):hDlg,&rRef);
			if(resizeInfo[i].flags&RESIZE_FLAG_SAME_DISTANCE_CONTROL_POS_TOP_POS_Y)
				newSize.top=rRef.top-resizeInfo[i].reference.bottom;
			else if(resizeInfo[i].flags&RESIZE_FLAG_SAME_DISTANCE_CONTROL_POS_BOTTOM_POS_Y)
				newSize.top=rRef.bottom-resizeInfo[i].reference.bottom;
			else if(resizeInfo[i].flags&RESIZE_FLAG_SAME_DISTANCE_CONTROL_MIDDLE_POS_Y)
				newSize.top=((rRef.bottom+rRef.top)>>1)-resizeInfo[i].reference.bottom;
			else if(resizeInfo[i].flags&RESIZE_FLAG_RELATIVE_TO_CONTROL_POS_TOP_POS_Y)
				newSize.top=(((rRef.top-rParent.top)*resizeInfo[i].reference.bottom)>>12)+rParent.top;
			else if(resizeInfo[i].flags&RESIZE_FLAG_RELATIVE_TO_CONTROL_POS_BOTTOM_POS_Y)
				newSize.top=(((rParent.bottom-rRef.bottom)*resizeInfo[i].reference.bottom)>>12)+rParent.top;
		}
		else if(resizeInfo[i].flags&RESIZE_FLAG_FIXED_POS_Y)
		{	newSize.top=resizeInfo[i].reference.bottom;
		}
		else newSize.top=rCtrl.top;

		if(resizeInfo[i].flags&RESIZE_MASK_WIDTH_NEED_REF)
		{	GetWindowRect(resizeInfo[i].refWidthControl?GetDlgItem(hDlg,resizeInfo[i].refWidthControl):hDlg,&rRef);
			if((resizeInfo[i].flags&RESIZE_FLAG_SAME_DISTANCE_CONTROL_POS_LEFT_WIDTH) && (resizeInfo[i].flags&RESIZE_FLAG_SAME_DISTANCE_CONTROL_POS_RIGHT_WIDTH))
				newSize.right=rRef.right-rRef.left;
			else if(resizeInfo[i].flags&RESIZE_FLAG_SAME_DISTANCE_CONTROL_POS_LEFT_WIDTH)
				newSize.right=rRef.left-resizeInfo[i].reference.left-newSize.left;
			else if(resizeInfo[i].flags&RESIZE_FLAG_SAME_DISTANCE_CONTROL_POS_RIGHT_WIDTH)
				newSize.right=rRef.right-resizeInfo[i].reference.left-newSize.left;
			else if(resizeInfo[i].flags&RESIZE_FLAG_SAME_DISTANCE_CONTROL_MIDDLE_WIDTH)
				newSize.right=((rRef.right+rRef.left)>>1)+resizeInfo[i].reference.left-newSize.left;
			else if(resizeInfo[i].flags&RESIZE_FLAG_RELATIVE_TO_WIDTH_WIDTH)
				newSize.right=((rRef.right-rRef.left)*resizeInfo[i].reference.left)>>12;
			else if(resizeInfo[i].flags&RESIZE_FLAG_RELATIVE_TO_POS_LEFT_WIDTH)
				newSize.right=((rRef.left-rParent.left)*resizeInfo[i].reference.left)>>12;
			else if(resizeInfo[i].flags&RESIZE_FLAG_RELATIVE_TO_POS_RIGHT_WIDTH)
				newSize.right=((rParent.right-rRef.right)*resizeInfo[i].reference.left)>>12;
		}
		else if(resizeInfo[i].flags&RESIZE_FLAG_FIXED_WIDTH)
			newSize.right=resizeInfo[i].reference.left;
		else
			newSize.right=rCtrl.right-rCtrl.left;

		if(resizeInfo[i].flags&RESIZE_MASK_HEIGHT_NEED_REF)
		{	GetWindowRect(resizeInfo[i].refHeightControl?GetDlgItem(hDlg,resizeInfo[i].refHeightControl):hDlg,&rRef);
			if((resizeInfo[i].flags&RESIZE_FLAG_SAME_DISTANCE_CONTROL_POS_TOP_HEIGHT) && (resizeInfo[i].flags&RESIZE_FLAG_SAME_DISTANCE_CONTROL_POS_BOTTOM_HEIGHT))
				newSize.bottom=rRef.bottom-rRef.top;
			else if(resizeInfo[i].flags&RESIZE_FLAG_SAME_DISTANCE_CONTROL_POS_TOP_HEIGHT)
				newSize.bottom=rRef.top-resizeInfo[i].reference.top-newSize.top;
			else if(resizeInfo[i].flags&RESIZE_FLAG_SAME_DISTANCE_CONTROL_POS_BOTTOM_HEIGHT)
				newSize.bottom=rRef.bottom-resizeInfo[i].reference.top-newSize.top;
			else if(resizeInfo[i].flags&RESIZE_FLAG_SAME_DISTANCE_CONTROL_MIDDLE_HEIGHT)
				newSize.bottom=((rRef.bottom+rRef.top)>>1)+resizeInfo[i].reference.top-newSize.top;
			else if(resizeInfo[i].flags&RESIZE_FLAG_RELATIVE_TO_HEIGHT_HEIGHT)
				newSize.bottom=((rRef.bottom-rRef.top)*resizeInfo[i].reference.top)>>12;
			else if(resizeInfo[i].flags&RESIZE_FLAG_RELATIVE_TO_POS_TOP_HEIGHT)
				newSize.bottom=((rRef.top-rParent.top)*resizeInfo[i].reference.top)>>12;
			else if(resizeInfo[i].flags&RESIZE_FLAG_RELATIVE_TO_POS_BOTTOM_HEIGHT)
				newSize.bottom=((rParent.bottom-rRef.bottom)*resizeInfo[i].reference.top)>>12;
		}
		else if(resizeInfo[i].flags&RESIZE_FLAG_FIXED_HEIGHT)
			newSize.bottom=resizeInfo[i].reference.top;
		else
			newSize.bottom=rCtrl.bottom-rCtrl.top;
		newSize.left -= delta.x;
		newSize.top -= delta.y;
		MoveWindow(GetDlgItem(hDlg,resizeInfo[i].ctrlId),newSize.left,newSize.top,newSize.right,newSize.bottom,0);
	}
}

void resizeInit(HWND hDlg,resize_info_t *resize_tmp)
{	RECT r1,r2,r3;
	int i;
	GetWindowRect(hDlg,&r3);
	for(i=0;resize_tmp[i].ctrlId;i++)
	{	GetWindowRect(GetDlgItem(hDlg,resize_tmp[i].ctrlId),&r2);
		if(resize_tmp[i].refWidthControl)	GetWindowRect(GetDlgItem(hDlg,resize_tmp[i].refWidthControl),&r1);
		else	GetWindowRect(hDlg,&r1);
		if(resize_tmp[i].flags&RESIZE_FLAG_FIXED_WIDTH)
		{	resize_tmp[i].reference.left=r2.right-r2.left;
		}
		else if((resize_tmp[i].flags&RESIZE_FLAG_SAME_DISTANCE_CONTROL_POS_LEFT_WIDTH) && (resize_tmp[i].flags&RESIZE_FLAG_SAME_DISTANCE_CONTROL_POS_RIGHT_WIDTH))
		{	resize_tmp[i].reference.left=r1.right-r1.left;
		}
		else if(resize_tmp[i].flags&RESIZE_FLAG_SAME_DISTANCE_CONTROL_POS_LEFT_WIDTH)
		{	resize_tmp[i].reference.left=r1.left-r2.right;
		}
		else if(resize_tmp[i].flags&RESIZE_FLAG_SAME_DISTANCE_CONTROL_POS_RIGHT_WIDTH)
		{	resize_tmp[i].reference.left=r1.right-r2.right;
		}
		else if(resize_tmp[i].flags&RESIZE_FLAG_SAME_DISTANCE_CONTROL_MIDDLE_WIDTH)
		{	resize_tmp[i].reference.left=r2.right-((r1.right+r1.left)/2);
		}
		else if(resize_tmp[i].flags&RESIZE_FLAG_RELATIVE_TO_WIDTH_WIDTH)
		{	resize_tmp[i].reference.left=((r2.right-r2.left)*4096)/(r1.right-r1.left);
		}
		else if(resize_tmp[i].flags&RESIZE_FLAG_RELATIVE_TO_POS_LEFT_WIDTH)
		{	resize_tmp[i].reference.left=((r2.right-r2.left)*4096)/(r1.left-r3.left);
		}
		else if(resize_tmp[i].flags&RESIZE_FLAG_RELATIVE_TO_POS_RIGHT_WIDTH)
		{	resize_tmp[i].reference.left=((r2.right-r2.left)*4096)/(r3.right-r1.right);
		}
		else resize_tmp[i].reference.left=0;

		if(resize_tmp[i].refHeightControl)	GetWindowRect(GetDlgItem(hDlg,resize_tmp[i].refHeightControl),&r1);
		else	GetWindowRect(hDlg,&r1);
		if(resize_tmp[i].flags&RESIZE_FLAG_FIXED_HEIGHT)
		{	resize_tmp[i].reference.top=r2.bottom-r2.top;
		}
		else if((resize_tmp[i].flags&RESIZE_FLAG_SAME_DISTANCE_CONTROL_POS_TOP_HEIGHT) && (resize_tmp[i].flags&RESIZE_FLAG_SAME_DISTANCE_CONTROL_POS_BOTTOM_HEIGHT))
		{	resize_tmp[i].reference.top=r1.bottom-r1.top;
		}
		else if(resize_tmp[i].flags&RESIZE_FLAG_SAME_DISTANCE_CONTROL_POS_TOP_HEIGHT)
		{	resize_tmp[i].reference.top=r1.top-r2.bottom;
		}
		else if(resize_tmp[i].flags&RESIZE_FLAG_SAME_DISTANCE_CONTROL_POS_BOTTOM_HEIGHT)
		{	resize_tmp[i].reference.top=r1.bottom-r2.bottom;
		}
		else if(resize_tmp[i].flags&RESIZE_FLAG_SAME_DISTANCE_CONTROL_MIDDLE_HEIGHT)
		{	resize_tmp[i].reference.top=r2.bottom-((r1.bottom+r1.top)/2);
		}
		else if(resize_tmp[i].flags&RESIZE_FLAG_RELATIVE_TO_HEIGHT_HEIGHT)
		{	resize_tmp[i].reference.top=((r2.bottom-r2.top)*4096)/(r1.bottom-r1.top);
		}
		else if(resize_tmp[i].flags&RESIZE_FLAG_RELATIVE_TO_POS_TOP_HEIGHT)
		{	resize_tmp[i].reference.top=((r2.bottom-r2.top)*4096)/(r1.top-r3.top);
		}
		else if(resize_tmp[i].flags&RESIZE_FLAG_RELATIVE_TO_POS_BOTTOM_HEIGHT)
		{	resize_tmp[i].reference.top=((r2.bottom-r2.top)*4096)/(r3.bottom-r1.bottom);
		}
		else resize_tmp[i].reference.top=0;

		if(resize_tmp[i].refPosXControl)	GetWindowRect(GetDlgItem(hDlg,resize_tmp[i].refPosXControl),&r1);
		else	GetWindowRect(hDlg,&r1);
		if(resize_tmp[i].flags&RESIZE_FLAG_FIXED_POS_X)
		{	resize_tmp[i].reference.right=r2.left-r3.left;
		}
		else if(resize_tmp[i].flags&RESIZE_FLAG_SAME_DISTANCE_CONTROL_POS_LEFT_POS_X)
		{	resize_tmp[i].reference.right=r1.left-r2.left;
		}
		else if(resize_tmp[i].flags&RESIZE_FLAG_SAME_DISTANCE_CONTROL_POS_RIGHT_POS_X)
		{	resize_tmp[i].reference.right=r1.right-r2.left;
		}
		else if(resize_tmp[i].flags&RESIZE_FLAG_SAME_DISTANCE_CONTROL_MIDDLE_POS_X)
		{	resize_tmp[i].reference.right=(r1.right+r1.left)/2-r2.left;
		}
		else if(resize_tmp[i].flags&RESIZE_FLAG_RELATIVE_TO_CONTROL_POS_LEFT_POS_X)
		{	resize_tmp[i].reference.right=((r2.left-r3.left)*4096)/(r1.left-r3.left);
		}
		else if(resize_tmp[i].flags&RESIZE_FLAG_RELATIVE_TO_CONTROL_POS_RIGHT_POS_X)
		{	resize_tmp[i].reference.right=((r2.left-r3.left)*4096)/(r3.right-r1.right + (r3.right==r1.right));
		}
		else resize_tmp[i].reference.right=0;

		if(resize_tmp[i].refPosYControl)	GetWindowRect(GetDlgItem(hDlg,resize_tmp[i].refPosYControl),&r1);
		else	GetWindowRect(hDlg,&r1);
		if(resize_tmp[i].flags&RESIZE_FLAG_FIXED_POS_Y)
		{	resize_tmp[i].reference.bottom=r2.top-r3.top;
		}
		else if(resize_tmp[i].flags&RESIZE_FLAG_SAME_DISTANCE_CONTROL_POS_TOP_POS_Y)
		{	resize_tmp[i].reference.bottom=r1.top-r2.top;
		}
		else if(resize_tmp[i].flags&RESIZE_FLAG_SAME_DISTANCE_CONTROL_POS_BOTTOM_POS_Y)
		{	resize_tmp[i].reference.bottom=r1.bottom-r2.top;
		}
		else if(resize_tmp[i].flags&RESIZE_FLAG_SAME_DISTANCE_CONTROL_MIDDLE_POS_Y)
		{	resize_tmp[i].reference.bottom=(r1.bottom+r1.top)/2-r2.top;
		}
		else if(resize_tmp[i].flags&RESIZE_FLAG_RELATIVE_TO_CONTROL_POS_TOP_POS_Y)
		{	resize_tmp[i].reference.bottom=((r2.top-r3.top)*4096)/(r1.top-r3.top);
		}
		else if(resize_tmp[i].flags&RESIZE_FLAG_RELATIVE_TO_CONTROL_POS_BOTTOM_POS_Y)
		{	resize_tmp[i].reference.bottom=((r2.top-r3.top)*4096)/(r3.bottom-r1.bottom);
		}
		else resize_tmp[i].reference.bottom=0;
		log(LOG_DEBUG,LD_APP,"CtrlId: %u, reference.left: %ld, reference.top: %ld, reference.right: %ld, reference.bottom: %ld",resize_tmp[i].ctrlId,resize_tmp[i].reference.left,resize_tmp[i].reference.top,resize_tmp[i].reference.right,resize_tmp[i].reference.bottom);
	}
}

void resizeChildDialog(HWND hParent,HWND hChild,resize_info_t *resizeInfo)
{	RECT tmpRect;POINT p1;
	GetClientRect(hParent,&childDlg);
	GetWindowRect(GetDlgItem(hParent,200),&tmpRect);
	tmpRect.right -= tmpRect.left;
	tmpRect.bottom -= tmpRect.top;
	p1.x=tmpRect.left;p1.y=tmpRect.top;
	ScreenToClient(hParent,&p1);
	childDlg.right -= childDlg.left + p1.x + tmpRect.right + 2;
	childDlg.left = p1.x + tmpRect.right + 2;
	childDlg.top = 0;
	childDlg.bottom = p1.y + tmpRect.bottom + 2;
	if(resizeInfo && resizeInfo[0].ctrlId==0)	resizeInfo=get_resize_info(childDlg,resizeInfo[0].refWidthControl);
	if(resizeInfo && resizeInfo[0].reference.left==-1)	resizeInit(hChild,resizeInfo);
	MoveWindow(hChild,childDlg.left,childDlg.top,childDlg.right,childDlg.bottom,0);
	if(resizeInfo)	resizeDialogControls(hChild,resizeInfo);
}
